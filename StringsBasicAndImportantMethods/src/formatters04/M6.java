package formatters04;

public class M6 {
public static void main(String[] args) {
		
		int i = 100;
		double j = 2.5;
		boolean k = true;
		String s1 = "abc";
		char c1 = 't';
		
		System.out.printf("i value is %d", i);	
		System.out.printf("\nj value is %s", j);
		System.out.printf("\nk value is %s", k);
		System.out.printf("\ns1 value is %s", s1);
		System.out.printf("\nsc1 value is %s", c1);
	}
}

